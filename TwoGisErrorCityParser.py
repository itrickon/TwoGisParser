class TwoGisCityChecker:
    def __init__(self, cities: List[str], keyword: str = "–®—É–±—ã", max_concurrent: int = 3):
        self.cities = cities
        self.keyword = keyword
        self.max_concurrent = max_concurrent
        self.results_dir = "2gis_check_results"
        self.failed_cities_file = f"{self.results_dir}/failed_cities.json"
        self.successful_cities = []  # –ì–æ—Ä–æ–¥–∞, –∫–æ—Ç–æ—Ä—ã–µ –Ω–∞—à–ª–∏—Å—å
        self.failed_cities = []      # –ì–æ—Ä–æ–¥–∞, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ –Ω–∞—à–ª–∏—Å—å
        self.errors = []             # –û—à–∏–±–∫–∏ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ
        
        # –°–æ–∑–¥–∞–µ–º –ø–∞–ø–∫—É –¥–ª—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
        os.makedirs(self.results_dir, exist_ok=True)
        
        # –û—á–∏—â–∞–µ–º —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –≥–æ—Ä–æ–¥–æ–≤
        if os.path.exists(self.failed_cities_file):
            os.remove(self.failed_cities_file)

    async def random_delay(self, min_seconds=1, max_seconds=3):
        await asyncio.sleep(random.uniform(min_seconds, max_seconds))

    async def translate_city_name(self, city_name: str) -> str:
        is_english = bool(re.match(r'^[a-zA-Z\s\-]+$', city_name))
        
        try:
            if city_name in city_mapping:
                return city_mapping[city_name]
        except:
            pass
        
        if is_english:
            city_clean = '-'.join(city_name.split())
            return city_clean.lower()
        else:
            translator = Translator()
            a = await translator.translate(city_name, src="ru", dest="en")
            a = '-'.join(a.text.split())
            return a.lower()

    async def check_city_in_url(self, url: str, city_code: str) -> bool:
        if city_code in url:
            return True
        
        patterns = [
            rf'https://2gis\.ru/{re.escape(city_code)}',
            rf'https://{re.escape(city_code)}\.2gis\.ru',
            rf'/{re.escape(city_code)}/',
        ]
        
        for pattern in patterns:
            if re.search(pattern, url):
                return True
        
        return False

    async def check_single_city(self, city: str, context) -> str:
        """–ü—Ä–æ–≤–µ—Ä—è–µ–º –æ–¥–∏–Ω –≥–æ—Ä–æ–¥. –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç 'found', 'not_found' –∏–ª–∏ 'error'"""
        page = None
        try:
            # –ü–æ–ª—É—á–∞–µ–º city_code –¥–ª—è –≥–æ—Ä–æ–¥–∞
            city_code = await self.translate_city_name(city)
            
            # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É
            page = await context.new_page()
            
            # –ü—Ä–æ–±—É–µ–º –∑–∞–≥—Ä—É–∑–∏—Ç—å –≥–ª–∞–≤–Ω—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É –≥–æ—Ä–æ–¥–∞
            city_url = f"https://2gis.ru/{city_code}"
            await self.random_delay(1, 2)
            try:
                response = await page.goto(city_url, wait_until="domcontentloaded", timeout=15000)
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å –æ—Ç–≤–µ—Ç–∞
                if response and response.status >= 400:
                    # –°—Ç—Ä–∞–Ω–∏—Ü–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ (404) –∏–ª–∏ –¥—Ä—É–≥–∞—è –æ—à–∏–±–∫–∞
                    return 'not_found'
                
                # –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–∏–π URL –ø–æ—Å–ª–µ –∑–∞–≥—Ä—É–∑–∫–∏
                current_url = page.url
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –≥–æ—Ä–æ–¥ –µ—Å—Ç—å –≤ URL
                city_in_url = await self.check_city_in_url(current_url, city_code)
                
                if not city_in_url:
                    return 'not_found'
                
                # –ì–æ—Ä–æ–¥ –Ω–∞–π–¥–µ–Ω
                return 'found'
                
            except Exception as e:
                # –û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
                if "Timeout" in str(e) or "net::ERR" in str(e):
                    return 'not_found'
                return 'error'
            
        except Exception as e:
            return 'error'
        
        finally:
            # –ó–∞–∫—Ä—ã–≤–∞–µ–º —Å—Ç—Ä–∞–Ω–∏—Ü—É
            if page and not page.is_closed():
                await page.close()

    async def check_city_batch(self, cities_batch: List[str], context):
        """–ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–∞–∫–µ—Ç –≥–æ—Ä–æ–¥–æ–≤ –ø–∞—Ä–∞–ª–ª–µ–ª—å–Ω–æ"""
        tasks = []
        for city in cities_batch:
            task = asyncio.create_task(self.check_single_city(city, context))
            tasks.append((city, task))
        
        # –û–∂–∏–¥–∞–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –≤—Å–µ—Ö –∑–∞–¥–∞—á
        for city, task in tasks:
            try:
                result = await task
                
                if result == 'found':
                    self.successful_cities.append(city)
                    print(f"‚úì –ù–∞–π–¥–µ–Ω: {city}")
                elif result == 'not_found':
                    self.failed_cities.append(city)
                    print(f"‚úó –ù–µ –Ω–∞–π–¥–µ–Ω: {city}")
                else:  # 'error'
                    self.errors.append(city)
                    print(f"‚ö† –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ: {city}")
                    
            except Exception as e:
                self.errors.append(city)
                print(f"‚ö† –ò—Å–∫–ª—é—á–µ–Ω–∏–µ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ {city}: {str(e)}")

    async def save_failed_cities(self):
        """–°–æ—Ö—Ä–∞–Ω—è–µ–º –Ω–µ –Ω–∞–π–¥–µ–Ω–Ω—ã–µ –≥–æ—Ä–æ–¥–∞ –≤ JSON —Ñ–∞–π–ª"""
        if not self.failed_cities:
            return
        
        failed_data = {
            'total_failed': len(self.failed_cities),
            'timestamp': time.strftime("%Y-%m-%d %H:%M:%S"),
            'keyword': self.keyword,
            'failed_cities': self.failed_cities
        }
        
        with open(self.failed_cities_file, 'w', encoding='utf-8') as f:
            json.dump(failed_data, f, ensure_ascii=False, indent=2)

    async def check_all_cities(self):
        """–û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤—Å–µ—Ö –≥–æ—Ä–æ–¥–æ–≤"""
        start_time = time.time()
        total_cities = len(self.cities)
        
        print(f"\n{'='*60}")
        print(f"–ü–†–û–í–ï–†–ö–ê –ì–û–†–û–î–û–í –í 2–ì–ò–°")
        print(f"{'='*60}")
        print(f"–í—Å–µ–≥–æ –≥–æ—Ä–æ–¥–æ–≤ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏: {total_cities}")
        print(f"–ö–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ: {self.keyword}")
        print(f"–û–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç—Å—è: {self.max_concurrent} –≥–æ—Ä–æ–¥–æ–≤")
        print(f"–†–µ–∂–∏–º: —Ç–æ–ª—å–∫–æ –ø—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è")
        print(f"{'='*60}")
        
        async with async_playwright() as playwright:
            # –ó–∞–ø—É—Å–∫–∞–µ–º –±—Ä–∞—É–∑–µ—Ä –≤ headless —Ä–µ–∂–∏–º–µ
            browser = await playwright.chromium.launch(
                headless=True,
                args=[
                    '--disable-gpu',
                    '--disable-dev-shm-usage',
                    '--no-sandbox'
                ]
            )
            
            # –°–æ–∑–¥–∞–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç –±—Ä–∞—É–∑–µ—Ä–∞
            context = await browser.new_context(
                user_agent="Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36",
                locale="ru-RU",
                viewport={'width': 1920, 'height': 1080}
            )
            
            # –†–∞–∑–±–∏–≤–∞–µ–º –≥–æ—Ä–æ–¥–∞ –Ω–∞ –ø–∞–∫–µ—Ç—ã
            batches = [self.cities[i:i + self.max_concurrent] 
                      for i in range(0, len(self.cities), self.max_concurrent)]
            
            total_batches = len(batches)
            
            for batch_num, city_batch in enumerate(batches, 1):
                print(f"\n–ü–∞–∫–µ—Ç {batch_num}/{total_batches}: –ø—Ä–æ–≤–µ—Ä–∫–∞ {len(city_batch)} –≥–æ—Ä–æ–¥–æ–≤")
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–∞–∫–µ—Ç –≥–æ—Ä–æ–¥–æ–≤
                await self.check_city_batch(city_batch, context)
                
                # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
                if batch_num % 5 == 0 or batch_num == total_batches:
                    await self.save_failed_cities()
                    processed = len(self.successful_cities) + len(self.failed_cities) + len(self.errors)
                    print(f"–ü—Ä–æ–≥—Ä–µ—Å—Å: {processed}/{total_cities} –≥–æ—Ä–æ–¥–æ–≤")
                
                # –ù–µ–±–æ–ª—å—à–∞—è –∑–∞–¥–µ—Ä–∂–∫–∞ –º–µ–∂–¥—É –ø–∞–∫–µ—Ç–∞–º–∏
                if batch_num < total_batches:
                    await self.random_delay(2, 3)
            
            # –ó–∞–∫—Ä—ã–≤–∞–µ–º –±—Ä–∞—É–∑–µ—Ä
            await browser.close()
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ñ–∏–Ω–∞–ª—å–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
        await self.save_failed_cities()
        
        # –í—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–∏
        end_time = time.time()
        total_time = end_time - start_time
        
        print(f"\n{'='*60}")
        print("–ò–¢–û–ì–ò –ü–†–û–í–ï–†–ö–ò:")
        print(f"{'='*60}")
        print(f"–í—Å–µ–≥–æ –ø—Ä–æ–≤–µ—Ä–µ–Ω–æ –≥–æ—Ä–æ–¥–æ–≤: {total_cities}")
        print(f"‚úì –ù–∞–π–¥–µ–Ω–æ –≤ 2–ì–ò–°: {len(self.successful_cities)}")
        print(f"‚úó –ù–µ –Ω–∞–π–¥–µ–Ω–æ –≤ 2–ì–ò–°: {len(self.failed_cities)}")
        print(f"‚ö† –û—à–∏–±–æ–∫ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ: {len(self.errors)}")
        print(f"–í—Ä–µ–º—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è: {total_time:.2f} —Å–µ–∫—É–Ω–¥")
        
        if self.failed_cities:
            print(f"\n–ù–µ –Ω–∞–π–¥–µ–Ω–Ω—ã–µ –≥–æ—Ä–æ–¥–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ —Ñ–∞–π–ª:")
            print(f"üìÅ {os.path.abspath(self.failed_cities_file)}")
            print(f"\n–°–ø–∏—Å–æ–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –≥–æ—Ä–æ–¥–æ–≤ ({len(self.failed_cities)}):")
            for i in range(0, len(self.failed_cities), 5):
                batch = self.failed_cities[i:i+5]
                print("  " + ", ".join(batch))
        
        if self.errors:
            print(f"\n–ì–æ—Ä–æ–¥–∞ —Å –æ—à–∏–±–∫–∞–º–∏ –ø—Ä–æ–≤–µ—Ä–∫–∏ ({len(self.errors)}):")
            for i in range(0, len(self.errors), 5):
                batch = self.errors[i:i+5]
                print("  " + ", ".join(batch))

def get_failed_cities_from_file(file_path: str = "2gis_check_results/failed_cities.json") -> List[str]:
    """–ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –≥–æ—Ä–æ–¥–æ–≤ –∏–∑ JSON —Ñ–∞–π–ª–∞"""
    if not os.path.exists(file_path):
        print(f"–§–∞–π–ª {file_path} –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        return []
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        failed_cities = data.get('failed_cities', [])
        print(f"–ó–∞–≥—Ä—É–∂–µ–Ω–æ {len(failed_cities)} –Ω–µ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –≥–æ—Ä–æ–¥–æ–≤ –∏–∑ —Ñ–∞–π–ª–∞")
        return failed_cities
        
    except Exception as e:
        print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Ñ–∞–π–ª–∞: {str(e)}")
        return []



